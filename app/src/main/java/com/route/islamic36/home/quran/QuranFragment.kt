        recyclerView = view.findViewById(R.id.recycler_view)
        adapter = SuraNameAdapter(chaptersNames)
        adapter.onitemClickListener = object : SuraNameAdapter.onItemClickListener{
            override fun onItemClick(pos: Int, item: String) {
                Toast.makeText(requireActivity(),item,Toast.LENGTH_SHORT).show()

                val intent = Intent(this@QuranFragment.getActivity(),SuraDetails::class.java)
                intent.putExtra("Sura_number",pos)
                startActivity(intent)

            }

        }
        recyclerView.adapter = adapter


